    public void baja() throws FileNotFoundException, IOException{ 
        
        File f = new File("Clientes.txt");
        if (f.exists()) {
            
            FileReader fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            String linea;
            int numL = 0;
            while((linea = br.readLine())!=null){
                numL++;
            }
            String[] _empleados=new String[numL];
            br= new BufferedReader(new FileReader(f));
            int o=0; 
            while((linea= br.readLine())!= null){
                
                _empleados[o]=linea;
                o++;
            }
            br.close();
            fr.close();
            FileWriter fw=new FileWriter(f);
            BufferedWriter bw= new BufferedWriter(fw);  
            for (int i = 0; i < _empleados.length; i++) { 
                if (selectorBaja.getSelectedIndex()==i) {
                    JOptionPane.showMessageDialog(null, "Eliminado exitosamente"); 
                    selectorBaja.setSelectedIndex(-1);
                } else {  
                    bw.write(_empleados[i]); 
                    bw.newLine();
                } 
            }  
            bw.close();
            fw.close();  
        } else {
             JOptionPane.showMessageDialog(null,"Error, archivo vacio");
        }
        
    }
    public void llenarSelectores() throws IOException{ 
        selectorBaja.removeAllItems();
        slctrAbono.removeAllItems();
        jTable1.setModel(new DefaultTableModel()); 
        try {
            File f=new File("Clientes.txt");
            FileReader fr= new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            String linea; 
            DefaultTableModel dm=new DefaultTableModel();
            dm.addColumn("Clientes"); 
            while((linea = br.readLine())!=null){
                String[] tmp=linea.split("%");
                String tmps="";
                for (int i = 0; i < tmp.length; i++) {
                tmps+=tmp[i]+" ";
                }
                selectorBaja.addItem(tmps); 
                slctrAbono.addItem(tmps); 
                dm.addRow(new Object[]{tmps});
            }
            jTable1.setModel(dm); 
        } catch (Exception e) {
        }
            
           
            
        
    }

