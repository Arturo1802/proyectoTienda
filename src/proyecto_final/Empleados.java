    private void llenarSelectores() { 
        selectorBaja.removeAllItems();
        jTable1.setModel(new DefaultTableModel());
        try {
            File f=new File("Empleados.txt");
            FileReader fr= new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            String linea; 
            DefaultTableModel dm=new DefaultTableModel();
            dm.addColumn("Empleados");
            dm.addColumn("Ventas");
            dm.addColumn("Salario neto"); 
            while((linea = br.readLine())!=null){
                String[] empTemporal=linea.split("%");
                String stringTemporal="";
                double ventas;
                if (empTemporal.length>5) {
                    for (int i = 0; i < empTemporal.length-1; i++) {
                        stringTemporal+=empTemporal[i]+" ";
                        
                    }  
                    ventas= Double.parseDouble(empTemporal[empTemporal.length-1]);
                } else {
                    for (int i = 0; i < empTemporal.length; i++) {
                        stringTemporal+=empTemporal[i]+" ";
                    } 
                    ventas=0;
                }
                selectorBaja.addItem(stringTemporal);    
                
                Empleado e=new Empleado(empTemporal[0], empTemporal[1].charAt(0), Persona.estadoCivilInt(empTemporal[2]) , empTemporal[3], new Fecha(empTemporal[4])); 
                e.setTotalVentas(ventas); 
                dm.addRow(new Object[]{stringTemporal,ventas,e.calculaSalarioNeto()});
            }
            br.close();
            jTable1.setModel(dm);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
