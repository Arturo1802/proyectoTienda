    private void altaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_altaActionPerformed
        String _nombre = null;
        try {
            File f= new File("Empleados.txt");
            if (f.exists()) {
                FileReader fr=new FileReader(f);
                BufferedReader br= new BufferedReader(fr);
                String linea;
                 
                while((linea = br.readLine())!=null){
                String empleado[] = linea.split("%");
                _nombre=empleado[1]+" ";
                }
                if (_nombre.equals(nombre.getText())) {
                    return;
                } 
            } else {
                System.out.println("No existe el archivo");
            }
        } catch (Exception e) {
        } 
        
        if(nombre.getText().equals("") || telefono.getText().equals("") || fecha.getText().equals("") ){}else{
            String a=sexo.getSelectedItem().toString();
            String[] fc=fecha.getText().split("/");
            int[] fec = new int[4];
            for (int i = 0; i < fc.length; i++) {
                String f=fc[i];
                fec[i]=Integer.valueOf(f);
            }

            
            Empleado e=new Empleado(nombre.getText(), a.charAt(0), estadoCivil.getSelectedIndex()+1, telefono.getText(), new Fecha(fec[0],fec[1] ,fec[2]));
            try {
                File f = new File("Empleados.txt" );
                FileWriter fw;
                BufferedWriter bw;
                BufferedReader br= new BufferedReader(new FileReader(f));
                if (f.exists()&& (br.readLine()!=null)) {
                    fw = new FileWriter(f,true);
                    bw= new BufferedWriter(fw);  
                    bw.write(e.nombre+"%"+e.sexo+"%"+Persona.estadoCivil(e)+"%"+e.telefono+"%"+e.getFecha().fechaLarga());
                    bw.newLine();
                    
                }else{
                    fw = new FileWriter(f);
                    bw= new BufferedWriter(fw);
                    bw.write(e.nombre+"%"+e.sexo+"%"+Persona.estadoCivil(e)+"%"+e.telefono+"%"+e.getFecha().fechaLarga());
                    bw.newLine();
                }
                bw.close();
                fw.close();
                llenarSelectores();
            } catch (Exception ex) {
                System.out.println(ex);
            }
        }
        nombre.setText("");
        sexo.setSelectedIndex(0);
        estadoCivil.setSelectedIndex(0);
        fecha.setText("");
        telefono.setText("");
    }//GEN-LAST:event_altaActionPerformed

