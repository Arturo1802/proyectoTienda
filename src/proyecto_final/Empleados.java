    public void baja() throws FileNotFoundException, IOException{  
        File f = new File("Empleados.txt");
        if (f.exists()) { 
            BufferedReader br=new BufferedReader(new FileReader(f));
            String linea;
            String[] _empleados=null;
            int ctrlLinea=0; 
            while((linea= br.readLine())!= null){ 
                _empleados[ctrlLinea]=linea;
                ctrlLinea++;
            }
            br.close(); 
            BufferedWriter bw= new BufferedWriter(new FileWriter(f));
            
            for (int i = 0; i < _empleados.length; i++) { 
                if (selectorBaja.getSelectedIndex()==i) {
                    JOptionPane.showMessageDialog(null, "Eliminado exitosamente");
                    selectorBaja.setSelectedIndex(-1);
                } else {    
                    bw.write(_empleados[i]); 
                    bw.newLine();
                } 
            }  
            bw.close(); 
        } else {
             JOptionPane.showMessageDialog(null,"Error, archivo vacio");
        }
        
    }
